export function useDocumentConnections(docId: string | undefined) {
  return useQuery({
    queryKey: ['document_connections', docId],
    queryFn: async () => {
      if (!docId) return { manual: [], auto: [] };

      const [{ data: manualConns, error: manualError }, { data: autoConns, error: autoError }] = await Promise.all([
        supabase
          .from('document_connections')
          .select('*')
          .or(`source_document_id.eq.${docId},target_document_id.eq.${docId}`),
        supabase
          .from('auto_generated_connections')
          .select('*')
          .or(`source_document_id.eq.${docId},target_document_id.eq.${docId}`),
      ]);

      if (manualError) throw manualError;
      if (autoError) throw autoError;

      return {
        manual: (manualConns || []) as DocumentConnection[],
        auto: (autoConns || []) as AutoGeneratedConnection[],
      };
    },
    enabled: !!docId,
  });
}

