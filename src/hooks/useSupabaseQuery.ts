import { useQuery } from '@tanstack/react-query';
import { supabase, Document, Event, AutoGeneratedConnection, DocumentConnection } from '@/lib/supabase';

export interface EnemyEntry {
  id: string;
  name: string;
  title: string;
  subtitle: string | null;
  severity: string;
  summary: string;
  key_facts: string[];
  status_tags: string[];
  evidence_documented: string;
  sources: Array<{ name: string; url: string; date: string }>;
  date_added: string;
  last_updated: string;
  footer_note?: string | null;
  sort_order?: number | null;
}

export interface PillarEssay {
  id: string;
  pillar_slug: string;
  title: string;
  subtitle: string | null;
  body_markdown: string;
  last_updated: string;
  word_count: number;
}

export function useDocuments() {
  return useQuery({
    queryKey: ['documents'],
    queryFn: async () => {
      const { data, error } = await supabase
        .from('documents')
        .select('*')
        .order('date_published', { ascending: false });
      if (error) throw error;
      return (data || []).map(doc => ({
        ...doc,
        topic_tags: (doc.metadata?.tags as string[]) || []
      })) as Document[];
    },
  });
}

export function useDocumentBySlug(slug: string | undefined) {
  return useQuery({
    queryKey: ['document', slug],
    queryFn: async () => {
      if (!slug) return null;
      const { data, error } = await supabase
        .from('documents')
        .select('*')
        .eq('slug', slug)
        .maybeSingle();
      if (error) throw error;
      return data ? ({
        ...data,
        topic_tags: (data.metadata?.tags as string[]) || []
      } as Document) : null;
    },
    enabled: !!slug,
  });
}

